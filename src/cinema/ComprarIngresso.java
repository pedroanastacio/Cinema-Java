    public void atualizaTabela(){
        DecimalFormat df = new DecimalFormat("0.00");  
        DefaultTableModel modelo = (DefaultTableModel) ListaSessaojTable.getModel();
        modelo.setNumRows(0);
        for(int i=0; i<cinema.sessoes.size(); i++){
            String tresD;
            if(cinema.sessoes.get(i).getTresD() == true){
                tresD = "Sim";
            } 
            else{
                tresD = "Não";
            }
            
            modelo.addRow(new Object[]{
                         cinema.sessoes.get(i).getNomeFilme(),
                         cinema.sessoes.get(i).getNomeSala(),
                         cinema.sessoes.get(i).getHorario(),
                         cinema.sessoes.get(i).getData(),
                         cinema.sessoes.get(i).getDubladoLegendado(),
                         tresD,
                         cinema.sessoes.get(i).getDiaSemana(),
                         df.format(cinema.sessoes.get(i).getValorIngresso()),
                         cinema.sessoes.get(i).getLotaçao()
            });
            
        }
    }
      
    
    public void atualizaSpinner(int maximo){
      SpinnerNumberModel model = new SpinnerNumberModel(0, 0, maximo, 1); 
      quantidadejSpinner.setModel(model);
      
      NumberEditor numbereditor = (NumberEditor)quantidadejSpinner.getEditor();
      numbereditor.getTextField().setEditable(false);
       
    }
    
    public void atualizaCampos(){
        inteirajRadioButton.setSelected(true);
        quantidadejSpinner.setValue(0);
    }
    
    public void geraFilmes(){
        for(int i=0; i<5; i++){
            Filme filme = new Filme();
            filme.setTitulo("Filme"+i);
            filme.setGenero("Genero"+i);
            filme.setDuraçao("16:00");
            filme.setClassIndicativa("Livre");
            
            cinema.filmes.add(filme);
        }
    }
    
    public void geraSessao(){
        for(int i=0; i<5; i++){
            Sessão sessao = new Sessão();
            sessao.setNomeFilme(cinema.filmes.get(i).getTitulo());
            sessao.setTresD(true);
            sessao.setHorario("21:00");
            sessao.setData("02/06/2019");
            sessao.setDubladoLegendado("Legendado");
            sessao.setNomeSala("Sala Vip 02");
            sessao.setDiaSemana("Sábado");
            sessao.calcValorIngresso(true, "Vip", "Sábado");
            sessao.calcLotaçao("Sala Vip 02");
            
            cinema.sessoes.add(sessao);
        }
    }
    
    @SuppressWarnings("static-access")
    public void vendeIngresso(){
        int posicao = ListaSessaojTable.getSelectedRow();
        String tipoIngresso;
        if(inteirajRadioButton.isSelected()){
            tipoIngresso = inteirajRadioButton.getText();
        }
        else{
            tipoIngresso = meiajRadioButton.getText();
        }
        
        for(int i=0; i<(Integer) quantidadejSpinner.getValue(); i++){
            Ingresso ingresso = new Ingresso();
            ingresso.setTipo(tipoIngresso);
            ingresso.calcPrecoIngresso(cinema.sessoes.get(posicao).getValorIngresso());
            ingresso.setDadosSessao(cinema.sessoes.get(posicao).getNomeFilme(), cinema.sessoes.get(posicao).getNomeSala(),
            cinema.sessoes.get(posicao).getData(), cinema.sessoes.get(posicao).getHorario(), cinema.sessoes.get(posicao).getTresD(), posicao);
            cinema.ingressos.add(ingresso);
            cinema.sessoes.get(posicao).calcLotaçao(cinema.sessoes.get(posicao).getNomeSala(), cinema.sessoes.get(posicao).getLotaçao(), true);
             
        }
    }
    
    public void construtor(){
        initComponents();
        this.getContentPane().setBackground(Color.BLACK);
        DefaultTableCellRenderer headerRenderer = new DefaultTableCellRenderer();
        headerRenderer.setBackground(Color.BLACK);
        headerRenderer.setForeground(Color.WHITE);
        for (int i = 0; i < ListaSessaojTable.getModel().getColumnCount(); i++) {
            ListaSessaojTable.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);
        }
        
        if(cinema.filmes.isEmpty())
            geraFilmes();
        if(cinema.sessoes.isEmpty())
            geraSessao();
        
        atualizaCampos();
        atualizaTabela();
    }
    
    
