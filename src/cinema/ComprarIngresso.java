    private void FinalizarjButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_FinalizarjButtonMouseClicked
        if(cinema.ingressos.isEmpty() && ListaSessaojTable.getSelectedRowCount() == 0){
            JOptionPane.showMessageDialog(null, "Por favor, selecione uma sessão para a qual deseja vender o(s) ingresso(s).");
        }
        else if(cinema.ingressos.isEmpty() && (Integer)quantidadejSpinner.getValue() == 0){
                     JOptionPane.showMessageDialog(null, "Por favor, selecione a quantidade de ingressos que deseja vender.");
            }
            else{
                if(ListaSessaojTable.getSelectedRowCount() == 0){
                    for(int i=0; i<cinema.ingressos.size(); i++){
                       venda.calcPrecoTotal(cinema.ingressos.get(i).getPrecoIngresso());
                    }

                    new FormVenda(venda).setVisible(true);
                    this.dispose();
                }
                else{
                    vendeIngresso();            
                    for(int i=0; i<cinema.ingressos.size(); i++){
                       venda.calcPrecoTotal(cinema.ingressos.get(i).getPrecoIngresso());
                    }

                    new FormVenda(venda).setVisible(true);
                    this.dispose();
                }
            }    
    }//GEN-LAST:event_FinalizarjButtonMouseClicked

    private void CancelarjButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CancelarjButtonMouseClicked
        if(cinema.ingressos.isEmpty()){
            JOptionPane.showMessageDialog(null, "Não há venda para cancelar.");
        }
        else if(JOptionPane.showConfirmDialog(null, "Deseja cancelar a venda?","Cancelar venda", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            for(int i=0; i<cinema.ingressos.size(); i++){
               cinema.sessoes.get(cinema.ingressos.get(i).getPosicao())
                      .calcLotaçao(cinema.sessoes.get(cinema.ingressos.get(i).getPosicao()).getNomeSala(), 
                                   cinema.sessoes.get(cinema.ingressos.get(i).getPosicao()).getLotaçao(),
                                   false);
            }

            cinema.ingressos.clear();
            atualizaTabela();
        }
    }//GEN-LAST:event_CancelarjButtonMouseClicked

